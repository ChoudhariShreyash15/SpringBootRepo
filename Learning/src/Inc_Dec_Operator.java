public class Inc_Dec_Operator {
    public static void main(String[] args) {
        int x = 5;
        x++;
        System.out.println(x); //6
        x--;
        x++;
//        x=7;
        System.out.println(x); //6
        x++;
        System.out.println(x); //7
        x=8;
        System.out.println(x); //8
        x--;
        System.out.println(x); //7
    }

}
